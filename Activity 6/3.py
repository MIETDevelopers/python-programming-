#various data types in python

# 1.Numbers
a = 34  #integer
print(a,type(a))
b = 34.67  #floating
print(b,type(b))
c = 2+4j  #complex
print(c,type(c))

#2.Strings
str = 'helloPython'
print(str,type(str))

#3.Lists
l = ['apple',34,12.89,'hello']
print(l,type(l))

#4.Tuples
t = ('apple',55,34.6,'seat')
print(t,type(t))

#5.Sets
s = {12,34.4,56,'apple'}
print(s,type(s))

#6.Dictionary
dict = {'apple':20,'mango':34,'banana':10}
print(dict,type(dict))